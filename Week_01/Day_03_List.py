nums = [0, 1, 2, 3, 4, 5]

# 기본: [시작:끝]
print(nums[1:4])   # [1, 2, 3] (4번 인덱스인 숫자 4는 포함 안 됨!)

# 생략: [시작:] 또는 [:끝]
print(nums[2:])    # [2, 3, 4, 5] (2번부터 끝까지)
print(nums[:3])    # [0, 1, 2] (처음부터 3번 전까지)

# 응용: 마지막 요소 가져오기
print(nums[-1])    # 5
print(nums[:-1])   # [0, 1, 2, 3, 4] (마지막 하나 빼고 다)


